def parse(indata: str) -> tuple[list[list[str]], int, int]:
    infile = io.StringIO(indata)

    start_row = 0
    start_col = 0
    maze = []

    for row, line in enumerate(filter(bool, map(str.strip, infile))):
        rows = []

        for col, cell in enumerate(line):
            if cell == 'S':
                start_row = row
                start_col = col

            rows.append(cell)

        maze.append(rows)

    return maze, start_row, start_col


